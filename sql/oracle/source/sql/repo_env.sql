whenever sqlerror exit failure
whenever oserror exit failure

undefine gluent_repo_install
undefine gluent_repo_upgrade

col gluent_repo_upgrade new_value gluent_repo_upgrade
col gluent_repo_install new_value gluent_repo_install
select decode(count(*), 0, 'Y', 'N') as gluent_repo_install
     , decode(count(*), 0, 'N', 'Y') as gluent_repo_upgrade
  from dba_tables
 where owner = '&gluent_db_repo_user';

spool sql/repo_env.tmp replace
set serveroutput on feedback off
begin
    dbms_output.put_line(q'{-- Script generated by repo_env.sql}');
    dbms_output.put_line(q'{-- LICENSE_TEXT}');
    dbms_output.put_line(q'{--}');
    if '&gluent_repo_install' = 'Y' then
        dbms_output.put_line('@@install_repo_env.sql');
    else
        dbms_output.put_line('@@upgrade_repo_env.sql');
    end if;
end;
/
spool off

@@repo_env.tmp
