CREATE TABLE gl_double_no_nan_inf
( id  NUMBER(5)
, val DOUBLE PRECISION
);

INSERT INTO gl_double_no_nan_inf
SELECT id,
CASE id
  WHEN 1 THEN 0
  WHEN 2 THEN NULL
  WHEN 3 THEN 1
  WHEN 4 THEN -1
  WHEN 5 THEN 1.1
  WHEN 6 THEN -1.1
  WHEN 7 THEN 1234.1234567890123456789
  WHEN 8 THEN -1234.1234567890123456789
  WHEN 9 THEN 0.000000000000000000001
  WHEN 10 THEN -0.000000000000000000001
END val
FROM generated_ids
WHERE id <= 10;
;
