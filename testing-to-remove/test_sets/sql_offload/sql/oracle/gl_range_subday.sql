insert into gl_range_subday
   ( id, data, dt )
with pdates as (
   select distinct to_date(substr(partition_name, 2, 8), 'yyyymmdd') as dt
   from   all_tab_partitions
   where  table_owner = sys_context('userenv','current_schema')
   and    table_name  = 'GL_RANGE_SUBDAY'
   )
select rownum
,      dbms_random.string('u',30)
,      dt + numtodsinterval(dbms_random.value(0,86399), 'SECOND')
from   pdates
,     (select rownum as r from dual connect by rownum <= 10)
;
